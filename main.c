void StringConvert(const char *str1, char *str2, int iBigFlag)
{
    int i, j, k, m;

    i = 0;
    j = 0;
    k = 0;
    if (1 == iBigFlag)
    {
        m = 16;
    }
    else if (0 == iBigFlag)
    {
        m = 21;
    }
    else
    {
        m = 500;
    }
    while (1)
    {
        if (0x00 == str1[i])
        {
            str2[j] = 0;
            break;
        }
        else if (('\\' == str1[i]) && (('n' == str1[i+1])||('N' == str1[i+1])))
        {
            str2[j] = '\n';
            i += 2;
            j++;
            k = 0;
        }
        else
        {
            if (k < m)
            {
                str2[j] = str1[i];
                j++;
                k++;
            }
            i++;
        }
    }
}



