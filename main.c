int GetKeyBlockMs(U32 uiTimeOutMs)
{
    U32  uiStartTime;

    uiStartTime = sysGetTimerCount();
    while (1)
    {
        if ((sysGetTimerCount()-uiStartTime) > uiTimeOutMs)
        {
            return KEY_TIMEOUT;
        }
        if (0 != kbhit())
        {
            return kbGetKey();
        }
    }
}

