// 串口直接接收
int s_PortReceiveString(int PortNum, U08 *psRxdData,
              U32 uiExpLen, U32 uiTimeOutMs, U32 *puiOutLen)
{
    int iRet, iFlag;
    U32 uiReadCnt, uiTemp;
    U32 uiOldTimeMs, uiNewTimeMs;

    uiReadCnt = iFlag = 0;
    uiTemp = 5000;
    uiOldTimeMs = sysGetTimerCount();
    while (uiReadCnt < uiExpLen)
    {
        uiNewTimeMs = sysGetTimerCount();
        if ((uiNewTimeMs-uiOldTimeMs) >= uiTimeOutMs)
        {
            if (uiReadCnt > 0)
            {
                break;
            }
            return ERR_COMM_TIMEOUT;
        }

        iRet = portRecv(PortNum, psRxdData, uiTemp);
        if (0x00 == iRet)
        {    // 接收成功,继续
            uiTemp = 80;
            psRxdData++;
            uiReadCnt++;
        }
        else if (0xff != iRet)
        {    // 非超时错误,退出
            return iRet;
        }
    }   // end of while( uiReadCnt<uiExpLen

    if (puiOutLen != NULL)
    {
        *puiOutLen = uiReadCnt;
    }

    return 0;
}

