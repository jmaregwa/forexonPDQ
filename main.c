void SetupMenu(void)
{
    U08 byRet;
    static U08 byCurPage = 0;

    while (1)
    {
        lcdCls();
        lcdDisplayCE(0, 0, 0x81, "    设置菜单    ", "  Setup Menu    ");
        if (1 == g_iLangueSelect)
        {
            byRet = LcdGetItemBigTimeout((U08*)g_abySetupMenuChn,
                SETUP_MENU_NUM, 0, LCD_MODE_1ITEM_PER_LINE|LCD_EXIT_MODE1,
                RESPOND_TIMEOUT, &byCurPage);
        }
        else
        {
            byRet = LcdGetItemBigTimeout((U08*)g_abySetupMenuEng,
                SETUP_MENU_NUM, 0, LCD_MODE_1ITEM_PER_LINE|LCD_EXIT_MODE1,
                RESPOND_TIMEOUT, &byCurPage);
        }

        switch (byRet)
        {
        case 0:  // parameter error
            lcdGoto(0, 2);
            lcdPrintf("Fatal error!");
            while (1);
        case 1:
            CommTypeSelect();
            s_CommInitModule(&g_oCommCfgParam);
            SaveCommCfg(&g_oCommCfgParam);
            return;
        case 2:
            SetTcpIpParam(" 远端IP地址设置 ", " Set remote host");
            SaveCommCfg(&g_oCommCfgParam);
            return;
        case 3:
            CommParamSet();
            return;
        case 4:
            break;
        case 5:
            break;
        case 0xff:
            return ;
        case 0xfe:
            break;
        default:
            break;
        }
    }
}

