int PrintData(void)
{
    int iRet, iKey;

    while (1)
    {
        iRet = prnStart();
        if (PRN_OK == iRet)
        {
            return OK;
        }
        else
        {
            if (PRN_PAPEROUT == iRet)
            {
                lcdCls();
                lcdDisplayCE(0, 2, DISP_MEDIACY|DISP_CFONT, "打印机缺纸... ", "  PAPEROUT...   ");
                lcdDisplayCE(0, 4, DISP_MEDIACY|DISP_CFONT, "按<确认>打印  ", "  [ENTER]-PRINT ");
                lcdDisplayCE(0, 6, DISP_MEDIACY|DISP_CFONT, "按<取消>退出  ", "  [CANCEL]-EXIT ");
                sysBeef(2, 1000);
                do{
                    iKey = kbGetKey();
                } while ((KEY_ENTER!=iKey) && (KEY_CANCEL!=iKey));

                if (KEY_ENTER == iKey)
                {
                    continue;
                }
                else
                {
                    return PRN_PAPEROUT;
                }
            }

            DispPrnErrInfo(iRet);
            kbGetKey();
            return NO_DISP;
        }
    }
}


